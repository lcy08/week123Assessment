// Helper function to get questions by section
export const getQuestionsBySection = (sectionKey) => {
  return questionsData.filter((question) => question.section === sectionKey);
};

// Helper function to calculate section scores
export const calculateSectionScore = (sectionKey, answers) => {
  const sectionQuestions = getQuestionsBySection(sectionKey);
  const correctAnswers = sectionQuestions.filter(
    (question) => answers[question.id] === question.correctAnswer
  );

  return {
    correct: correctAnswers.length,
    total: sectionQuestions.length,
    percentage: Math.round(
      (correctAnswers.length / sectionQuestions.length) * 100
    ),
  };
};

