// Helper function to get questions by section
export const getQuestionsBySection = (sectionKey) => {
  return questionsData.filter(question => question.section === sectionKey);
};

// Helper function to calculate section scores
export const calculateSectionScore = (sectionKey, answers) => {
  const sectionQuestions = getQuestionsBySection(sectionKey);
  const correctAnswers = sectionQuestions.filter(
    question => answers[question.id] === question.correctAnswer
  );
  
  return {
    correct: correctAnswers.length,
    total: sectionQuestions.length,
    percentage: Math.round((correctAnswers.length / sectionQuestions.length) * 100)
  };
};

// Performance level definitions
export const getPerformanceLevel = (percentage) => {
  if (percentage >= 90) {
    return {
      level: 'Excellent',
      color: '#10b981',
      description: 'Performa luar biasa! Pertahankan kerja yang bagus ini.',
      recommendation: 'Terus berlatih untuk mempertahankan level yang sangat baik ini.'
    };
  }
  if (percentage >= 75) {
    return {
      level: 'Good',
      color: '#3b82f6',
      description: 'Pemahaman yang baik terhadap materi.',
      recommendation: 'Tinjau kembali topik yang terlewat dan berlatih soal yang lebih menantang.'
    };
  }
  if (percentage >= 60) {
    return {
      level: 'Fair',
      color: '#f59e0b',
      description: 'Pemahaman cukup. Disarankan untuk latihan tambahan.',
      recommendation: 'Fokus pada peninjauan konsep dasar dan berlatih secara teratur.'
    };
  }
  return {
    level: 'Needs Improvement',
    color: '#ef4444',
    description: 'Memerlukan dukungan dan latihan tambahan.',
    recommendation: 'Bekerja sama dengan guru untuk bantuan ekstra dan berlatih konsep dasar setiap hari.'
  };
};

