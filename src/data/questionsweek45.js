// Helper function to get questions by section
export const getQuestionsBySection = (sectionKey) => {
  return questionsData.filter((question) => question.section === sectionKey);
};

// Helper function to calculate section scores
export const calculateSectionScore = (sectionKey, answers) => {
  const sectionQuestions = getQuestionsBySection(sectionKey);
  const correctAnswers = sectionQuestions.filter(
    (question) => answers[question.id] === question.correctAnswer
  );

  return {
    correct: correctAnswers.length,
    total: sectionQuestions.length,
    percentage: Math.round(
      (correctAnswers.length / sectionQuestions.length) * 100
    ),
  };
};

// Performance level definitions
export const getPerformanceLevel = (percentage) => {
  if (percentage >= 90) {
    return {
      level: "Excellent",
      color: "#10b981",
      description:
        "Performa luar biasa! Siswa menguasai materi dengan sangat baik.",
      recommendation:
        "Lanjutkan ke materi yang lebih menantang dan pertahankan latihan rutin.",
    };
  }
  if (percentage >= 75) {
    return {
      level: "Good",
      color: "#3b82f6",
      description: "Pemahaman yang baik terhadap materi.",
      recommendation:
        "Tinjau kembali beberapa konsep dan berlatih aplikasi dalam konteks yang berbeda.",
    };
  }
  if (percentage >= 60) {
    return {
      level: "Fair",
      color: "#f59e0b",
      description:
        "Pemahaman cukup. Perlu latihan tambahan pada area tertentu.",
      recommendation:
        "Fokus pada peninjauan konsep dasar dan berlatih secara konsisten dengan contoh yang lebih banyak.",
    };
  }
  return {
    level: "Needs Improvement",
    color: "#ef4444",
    description: "Memerlukan bimbingan intensif dan latihan tambahan.",
    recommendation:
      "Ulangi materi dasar dengan pendekatan yang lebih visual dan interaktif. Pertimbangkan sesi review tambahan.",
  };
};

